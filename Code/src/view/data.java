    private void tsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tsimpanActionPerformed
        databarang obj1 = new databarang();
        String KategoriBuku = ktg_brg.getSelectedItem().toString();
        String idbarang = fid.getText();
        String namabarang = fnama.getText();
        String harga = fharga.getText();
        
        if(idbarang.isEmpty() || namabarang.isEmpty()){
            JOptionPane.showMessageDialog(this, "Harap edit data dengan benar",
                    "Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);      
        }else if(harga.isEmpty()){
            JOptionPane.showMessageDialog(this, "Harap edit data dengan benar",
                    "Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);
        }else{
        String[] pisahKategori = KategoriBuku.split("-");
        String KodeKategori = pisahKategori[0];
        String Kategori = pisahKategori[1];
        
        obj1.editBarang(idbarang, namabarang, harga, KodeKategori);
        tbl.getDataVector().removeAllElements();
        
        //get data tabel
        String [][] data = obj1.getDataBarang();
        for(int i=0; i<data.length; i++){
            tbl.addRow(new Object[]{data[i][0], data[i][1],data[i][2],data[i][3]});           
        }
        tbl_brg.setModel(tbl);
        
        JOptionPane.showMessageDialog(this, "Data berhasil diedit",
                    "Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);
        fid.setEditable(true);
        fid.setText("");
        fnama.setText("");
        fharga.setText("");
        }
    }//GEN-LAST:event_tsimpanActionPerformed

    private void teditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_teditActionPerformed
        databarang obj1 = new databarang();
        
        int Pilihbaris = tbl_brg.getSelectedRow();
        
        if(tbl_brg.isRowSelected(Pilihbaris)){
        String KodeBuku = tbl.getValueAt(Pilihbaris, 0).toString();
        String[][] data = obj1.getDataBarang(KodeBuku);        
        String nama_barang = data[0][1];
        String harga = data[0][2];
        String id_kategori = data[0][3];
        String nama_kategori = data[0][4];
        
        ktg_brg.setSelectedItem(id_kategori+"-"+nama_kategori);
        fid.setText(KodeBuku);
        fid.setEditable(false);
        fnama.setText(nama_barang);
        fharga.setText(harga);
        }else{
            JOptionPane.showMessageDialog(this, "Pilih data terlebih dahulu",
                "Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_teditActionPerformed

    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        databarang obj = new databarang();
        
        int Pilihbaris = tbl_brg.getSelectedRow();
        if(tbl_brg.isRowSelected(Pilihbaris)){
        String id_barang = tbl.getValueAt(Pilihbaris, 0).toString();
        
        obj.HapusDataBarang(id_barang);
        tbl.removeRow(Pilihbaris);
        JOptionPane.showMessageDialog(this, "Data berhasil dihapus",
                "Pesan Keonfirmasi", JOptionPane.INFORMATION_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(this, "Pilih data terlebih dahulu",
                "Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_hapusActionPerformed

