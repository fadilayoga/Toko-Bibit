    private void ttambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ttambahActionPerformed
        if(fid.getText().isEmpty()||fnama.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Harap isi data dengan lengkap",
                "Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);
        }else if(fharga.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Harap isi data dengan lengkap",
                "Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);
        }else{
            databarang obj = new databarang();
        String Cid_ktg = ktg_brg.getSelectedItem().toString();
        String id_barang = fid.getText();
        String namabarang = fnama.getText();
        String buy = fharga.getText();
        
        String[] pisahKategori = Cid_ktg.split("-");
        String id_Kategori = pisahKategori[0];
        String namaKategori = pisahKategori[1];
        
        if(id_barang.isEmpty()){
            JOptionPane.showMessageDialog(this, "id kategori harus diisi",
                "Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);
        }else if(namabarang.isEmpty()){
            JOptionPane.showMessageDialog(this, "Nama kategori harus diisi",
                "Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);
        }else{
            obj.TambahDataBarang(id_barang, namabarang, buy, id_Kategori);
            tbl.addRow(new Object[]{id_barang, namabarang, buy, id_Kategori});
            tbl_brg.setModel(tbl);
            fid.setText("");
            fnama.setText("");
            fharga.setText("");
        }
        }
    }//GEN-LAST:event_ttambahActionPerformed

