    private void ttambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ttambahActionPerformed
        if(nm_brg.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Harap pilih barang", 
        "Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);
        }else if(jml.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Tentukan jumlah barang", 
        "Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);
        }else{
        trans_brg obj = new trans_brg();
        String SJumlah = jml.getText();       
        int Pilihbaris = tbl_brg.getSelectedRow();
        String id_brg = tbl.getValueAt(Pilihbaris, 0).toString();
        String [][] data = obj.getDataBarang(id_brg);
        String nama_barang = data[0][0];
        String harga = data[0][1];        
        tbl2.addRow(new Object[]{nama_barang,harga,SJumlah});
        tbl_keranjang.setModel(tbl2);
        int jmlKolom = tbl_keranjang.getColumnCount();
        int brs = tbl_keranjang.getRowCount() - 1;
        int r = brs;
        String krj [][] = new String[brs + 5][jmlKolom];        
        for(int c=0; c<jmlKolom-1; c++){
            krj[r][c]= data[0][c];            
        }
        krj[r][2]= SJumlah;
        trans_brg.setJumlah(SJumlah, harga);
        takhir.add(obj.hitung_total());
        
        //hitung total
        int total = 0;    
        for(int i=0; i<takhir.size();i++){
        total += takhir.get(i);
        }
        String Total = Integer.toString(total);
        t_akhir.setText(Total);
        
        //clear form
        nm_brg.setText("");
        hrg.setText("");
        jml.setText("");
        }
    }//GEN-LAST:event_ttambahActionPerformed

