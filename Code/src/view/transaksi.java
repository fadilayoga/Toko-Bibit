    private void bbuyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bbuyActionPerformed
        if(tbl_keranjang.getRowCount()==0){
        JOptionPane.showMessageDialog(this, "Harus ada barang yang dibeli", 
        "Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);
        }else{
        trans_brg obj = new trans_brg();        
        int jmlKolom = tbl_keranjang.getColumnCount(); 
        int brs = tbl_keranjang.getRowCount() - 1;
        String total = t_akhir.getText();
        String krj [][] = new String[1000][jmlKolom];
        for(int r=0; r<=brs; r++){
            for(int c=0; c<jmlKolom; c++){ 
            krj[r][c]= tbl_keranjang.getValueAt(r, c).toString();  
        }
            String id_brg = obj.getID_Barang(krj[r][0], krj[r][1]);
            obj.keranjang(IDUser, id_brg, krj[r][2]);
        }
        JOptionPane.showMessageDialog(this, "Kode Pembayaran "+IDUser+Telp
                + "\nJumlah Yang Harus Dibayar Rp."+total, 
        "Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);
        t_akhir.setText("0");
        takhir.clear();
        tbl2.getDataVector().removeAllElements();
        tbl2.fireTableDataChanged();
        }
    }//GEN-LAST:event_bbuyActionPerformed

    private void bhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapusActionPerformed
        int kol = tbl_keranjang.getSelectedRow();
        if(tbl_keranjang.getRowCount()==0){
        JOptionPane.showMessageDialog(this, "Harus ada barang yang dibeli", 
        "Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);
        }else if(tbl_keranjang.isRowSelected(kol)){        
        tbl2.removeRow(kol);
        takhir.remove(kol);
        int total = 0;
        for(int i=0; i<takhir.size(); i++){
            total += takhir.get(i);
        }
        String Total = Integer.toString(total);
        t_akhir.setText(Total);
        }else{
        JOptionPane.showMessageDialog(this, "Pilih barang yang ingin dihapus", 
        "Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_bhapusActionPerformed

